def get_commands_by_category() -> dict[str, list[tuple]]:
    """Сгруппировать команды по категориям."""
    result: dict[str, list[tuple]] = {}
    for cmd in COMMANDS:
        cat = cmd[3]
        if cat not in result:
            result[cat] = []
        result[cat].append(cmd)
    return result


def get_command_index(cmd_name: str) -> int:
    """Получить индекс команды по имени."""
    for i, cmd in enumerate(COMMANDS):
        if cmd[0] == cmd_name:
            return i
    return -1


