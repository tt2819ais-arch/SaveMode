def menu_text() -> str:
    """Текст меню: категории с однострочными описаниями (как в референсе)."""
    lines = []
    grouped = get_commands_by_category()
    for cat_key, cat_title in CATEGORIES.items():
        cmds = grouped.get(cat_key, [])
        if not cmds:
            continue
        lines.append(f"▪️ <b>{cat_title}</b>")
        for name, short, _full, _cat in cmds:
            lines.append(f"• <code>{name}</code> — {short}")
        lines.append("")
    lines.append("Нажми на кнопку, чтобы узнать информацию о команде.")
    return "\n".join(lines)


def strip_lead_emoji(text: str) -> str:
    """Убрать ведущий декоративный эмодзи перед '<b>' в описании команды."""
    import re
    return re.sub(r"^[^\w<]*\s*(?=<b>)", "", text, count=1)


